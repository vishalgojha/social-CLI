function getBanner(style = 'slant') {
  if (style === 'clean') {
    return [
      'SOCIAL CLI',
      '----------',
      'Meta APIs from your terminal'
    ].join('\n');
  }

  if (style === 'compact') {
    return [
      '+------------------------------+',
      '|          SOCIAL CLI          |',
      '+------------------------------+'
    ].join('\n');
  }

  if (style === 'classic') {
    return [
      ' ____   ___   ____ ___    _    _       ____ _     ___ ',
      '/ ___| / _ \\ / ___|_ _|  / \\  | |     / ___| |   |_ _|',
      '\\___ \\| | | | |    | |  / _ \\ | |    | |   | |    | | ',
      ' ___) | |_| | |___ | | / ___ \\| |___ | |___| |___ | | ',
      '|____/ \\___/ \\____|___/_/   \\_\\_____| \\____|_____|___|'
    ].join('\n');
  }

  // Pre-generated banner; no runtime dependency.
  return [
    '  _____            _       _    ____ _     ___ ',
    ' / ____|          (_)     | |  / ___| |   |_ _|',
    '| (___   ___   ___ _  __ _| | | |   | |    | | ',
    ' \\___ \\ / _ \\ / __| |/ _` | | | |   | |    | | ',
    ' ____) | (_) | (__| | (_| | | | |___| |___ | | ',
    '|_____/ \\___/ \\___|_|\\__,_|_|  \\____|_____|___|'
  ].join('\n');
}

module.exports = {
  getBanner
};
