function getBanner(style = 'slant') {
  if (style === 'classic') {
    return [
      ' __  __      _          ____ _     ___',
      '|  \\/  | ___| |_ __ _  / ___| |   |_ _|',
      '| |\\/| |/ _ \\ __/ _` | |   | |    | |',
      '| |  | |  __/ || (_| | |___| |___ | |',
      '|_|  |_|\\___|\\__\\__,_|  \\____|_____|___|'
    ].join('\n');
  }

  // Pre-generated (figlet-like) banner; no runtime dependency.
  return [
    '    __  ___      __        ________    ________',
    '   /  |/  /___ _/ /_____ _/ ____/ /   /  _/ __ \\',
    '  / /|_/ / __ `/ __/ __ `/ /   / /    / // / / /',
    ' / /  / / /_/ / /_/ /_/ / /___/ /____/ // /_/ /',
    '/_/  /_/\\__,_/\\__/\\__,_/\\____/_____/___/\\____/'
  ].join('\n');
}

module.exports = {
  getBanner
};

